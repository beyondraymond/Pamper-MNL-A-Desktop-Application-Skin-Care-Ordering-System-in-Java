    public void getFirstName (int u) throws SQLException {
        Connection nameCon = javaconnect.connectdb();
        try (PreparedStatement ps = nameCon.prepareStatement("select FIRST_NAME from LOGINTABLE where USER_ID=?")){
            ps.setInt(1, u);
            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    currUser.setText(rs.getString("FIRST_NAME") + "!");
                }
            }
        }
        nameCon.close();
        }
