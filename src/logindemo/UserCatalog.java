    public int getTransID(){
        int transNum=0;
        PreparedStatement ps;
        Connection sCon = javaconnect.connectdb();
        try{
                ps = sCon.prepareStatement("SELECT MAX(TRANS_ID) FROM TRANSACTIONS");
                
                ResultSet rs = ps.executeQuery();
                if (rs.next()) {
                    transNum = rs.getInt(1);
                }
        }
        catch(Exception e){
            System.out.println("getTransError: " + e);
        }
        return transNum;
    }
    
