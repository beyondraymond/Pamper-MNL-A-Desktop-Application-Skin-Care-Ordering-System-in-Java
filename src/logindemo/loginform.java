    public int getUSERID (String u) throws SQLException {
        int num = 0;
        try (PreparedStatement ps = con.prepareStatement("select USER_ID from LOGINTABLE where USERNAME=?")){
            ps.setString(1, u);
            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    num = rs.getInt("USER_ID");
                }
            }
        }
        return num;
        }
    
    
