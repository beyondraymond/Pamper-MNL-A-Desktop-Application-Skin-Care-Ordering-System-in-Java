    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String login = "SELECT * FROM logintable WHERE username=? AND password=?";
        try{
            ps = con.prepareStatement(login);
            ps.setString(1, username.getText());
            ps.setString(2, password.getText());
            rs = ps.executeQuery();
            if(rs.next()){
                this.ActiveUser = username.getText();
                UserID = getUSERID(ActiveUser);
                if("admin".equals(ActiveUser)){
                    JOptionPane.showMessageDialog(null, "Login Successfull!");
                    new DatabaseForm(UserID).setVisible(true);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "Login Successfull!");
                    new UserCatalog(UserID).setVisible(true);
                    this.dispose();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Login Failed. Invalid Credentials");
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

